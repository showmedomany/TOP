package chat;

public class ChatDataBean {
	private String ip;
	private String chatting;
	private String id;
	
	
	public String getIp() {
		return ip;
	}
	public String getChatting() {
		return chatting;
	}
	public String getId() {
		return id;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public void setChatting(String chatting) {
		this.chatting = chatting;
	}
	public void setId(String id) {
		this.id = id;
	}	
}
