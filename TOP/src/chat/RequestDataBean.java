package chat;

public class RequestDataBean {
	private String ip;
	private String id;
	private int response;
	
	
	public String getIp() {
		return ip;
	}
	public String getId() {
		return id;
	}
	public int getResponse() {
		return response;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setResponse(int response) {
		this.response = response;
	}	
}
